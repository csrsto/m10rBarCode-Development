    private void jButtonBarCodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBarCodeActionPerformed
        // TODO add your handling code here:
        Barcode barcode = null;
        String namecode = "";
        try {
            barcode = BarcodeFactory.createCode128(txtCode.getText());
            namecode = (txtName.getText());
        } catch (Exception e) {
        }
        barcode.setDrawingText(true);
        barcode.setBarWidth(2);
        barcode.setBarHeight(60);
        BufferedImage image = new BufferedImage(500, 100, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g = (Graphics2D) image.getGraphics();
        try {
            barcode.draw(g, 5, 20);
        } catch (Exception e) {
        }
        ImageIcon icon = new ImageIcon(image);
        txtBarCode.setIcon(icon);
        txtBarName.setText(namecode);
    }//GEN-LAST:event_jButtonBarCodeActionPerformed

